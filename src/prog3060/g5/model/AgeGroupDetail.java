package prog3060.g5.model;

public class AgeGroupDetail {
	String description;
	int malePopulationOld;
	int malePopulationNew;
	int femalePopulationOld;
	int femalePopulationNew;
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getMalePopulationOld() {
		return malePopulationOld;
	}
	public void setMalePopulationOld(int malePopulationOld) {
		this.malePopulationOld = malePopulationOld;
	}
	public int getMalePopulationNew() {
		return malePopulationNew;
	}
	public void setMalePopulationNew(int malePopulationNew) {
		this.malePopulationNew = malePopulationNew;
	}
	public int getFemalePopulationOld() {
		return femalePopulationOld;
	}
	public void setFemalePopulationOld(int femalePopulationOld) {
		this.femalePopulationOld = femalePopulationOld;
	}
	public int getFemalePopulationNew() {
		return femalePopulationNew;
	}
	public void setFemalePopulationNew(int femalePopulationNew) {
		this.femalePopulationNew = femalePopulationNew;
	}
}
